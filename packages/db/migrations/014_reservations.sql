-- store_hours: configurable business hours per store per day-of-week
CREATE TABLE IF NOT EXISTS store_hours (
  id TEXT PRIMARY KEY DEFAULT (lower(hex(randomblob(16)))),
  store_key TEXT NOT NULL,
  day_of_week INTEGER NOT NULL, -- 0=Sun, 1=Mon, 2=Tue, 3=Wed, 4=Thu, 5=Fri, 6=Sat
  open_time TEXT NOT NULL DEFAULT '10:00',
  close_time TEXT NOT NULL DEFAULT '20:00',
  is_closed INTEGER NOT NULL DEFAULT 0,
  UNIQUE(store_key, day_of_week)
);

-- store_reservations: customer visit reservations
CREATE TABLE IF NOT EXISTS store_reservations (
  id TEXT PRIMARY KEY DEFAULT (lower(hex(randomblob(16)))),
  friend_id TEXT,
  line_user_id TEXT NOT NULL,
  store_key TEXT NOT NULL,
  date TEXT NOT NULL,
  time TEXT NOT NULL,
  name TEXT NOT NULL,
  phone TEXT NOT NULL DEFAULT '',
  notes TEXT NOT NULL DEFAULT '',
  status TEXT NOT NULL DEFAULT 'pending',
  created_at TEXT NOT NULL DEFAULT (datetime('now'))
);

